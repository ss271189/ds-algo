package com.ss.practice.operator;

public class BitwiseOperatorDemo {

    public static void main(String[] args) {
        int a=4;
        int b=6;
        System.out.println(" a&b :"+(a&b));
        System.out.println(" a|b :"+(a|b));
        System.out.println(" ~a :"+(~a));
        System.out.println(" a^b :"+(a^b));
    }
}
